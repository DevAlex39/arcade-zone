export function useI18n() {
  const platform = usePlatformStore();

  function t(key, params = {}) {
    const entry = T[key];
    if (!entry) return key;
    let str = entry[platform.lang] ?? entry.fr ?? key;
    for (const [k, v] of Object.entries(params)) {
      str = str.replace(`{${k}}`, v);
    }
    return str;
  }

  return { t };
}
